# Dat fiind string-ul
s = "Este o dimineață minunată de mers la plajă."

# faceți print:

# - primelor 4 caractere
s[:4]

# - ultimelor 6 caractere
s[-6:]

# - caracterelor de la început până la ultimele 6
s[:-6]

# - caracterelor dintre index-ul 16 și 25, inclusiv
s[16:26]

# - fiecărui al 2lea caracter
s[::2]

# - ultimelor 6 caractere, citite de la coadă la cap
s[:-7:-1]

# - întregului string, de la coadă la cap
s[::-1]
